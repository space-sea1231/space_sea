bool Check1(int x, int y, int n, int m) {
  return x >= 1 && x <= n && y >= 1 && y <= m;
}
bool ttmp;
int Check(int n, int m) {
  int cnt = 0;
  for (int i = 1; i <= n; i++) {
    for (int j = 1; j <= m; j++) {
      if (a[i][j] != 1) continue;
      for (int k = 0; k < 8; k++) {
        int x = i, y = j;
        bool flag = true;
        for (int l = 1; l <= 2; l++) {
          x += dx[k];
          y += dy[k];
          if (Check1(x, y, n, m)) {
            if (a[x][y] != tmp[l]) {
              flag = false;
              break;
            }
          }
          else{
            flag = false;
            break;
          }
        }
        if (flag == true) cnt++;
      }
    }
  }
  return cnt;
}

